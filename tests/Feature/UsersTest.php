use Tests\TestCase;

class UsersTest extends TestCase
{
    use RefreshDatabase;

    protected $form;
    protected $user;
    protected $other;

    protected function setUp(): void
    {
        parent::setUp(); // TODO: Change the autogenerated stub

        $email = "1234";
        $password = "1234";

        $this->user = User::factory()->create([
            "email" => $email,
            "password" => Hash::make($password)
        ]);

        $this->other = User::factory()->create([
            "email" => $email,
            "password" => Hash::make($password)
        ]);

        $this->actingAs($this->user);

        VerifyNumber::create(['ids' => "01012341234", 'verified' => 1, 'number' => '123']);

        $this->form = [
            'email' => "123@naver.com",
            'password' => "123@naver.com",
            'password_confirmation' => "123@naver.com",
            'name' => "123@naver.com",
            'contact' => "01012341234",
            'agree_promotion' => 0,
        ];
    }

    /** @test */
    public function 누구나_생성을_할_수_있다()
    {
        /*
         * email
         * password
         * password_confirmation
         * name
         * contact (verified 된 contact)
         * agree_promotion
         * */

        $count = User::count();

        $this->json('post', '/api/users', $this->form);

        $this->assertEquals($count + 1, User::count());
    }

    /** @test */
    public function 인증된_전화번호로만_생성을_할_수_있다()
    {
        VerifyNumber::where('ids', $this->form['contact'])->delete();

        $this->json('post', '/api/users', $this->form)->assertStatus(422);
    }

    /** @test */
    public function 사용자는_추천인코드를_업데이트할_수_있다()
    {
        $point = $this->user->point;

        // code
        $this->patch('/api/users/codeRecommend', [
            'code_recommend' => $this->other->code
        ])->assertStatus(200);

        $this->assertEquals($point + User::$recommendPoint, $this->user->refresh()->point);
    }

    /** @test */
    public function 존재하지_않는_추천인코드는_업데이트할_수_없다()
    {
        $point = $this->user->point;

        // code
        $this->patch('/api/users/codeRecommend', [
            'code_recommend' => '0000000000000'
        ])->assertStatus(403);
    }

    /** @test */
    public function 이미_추천인코드가_있다면_업데이트할_수_없다()
    {
        $this->user->update(['code_recommend' => '123']);

        // code
        $this->patch('/api/users/codeRecommend', [
            'code_recommend' => $this->other->code
        ])->assertStatus(403);
    }

    /** @test */
    public function 추천인코드가_업데이트되면_추천자와_추천받은자에게_적립금이_지급된다()
    {
        $pointUser = $this->user->point;
        $pointOther = $this->other->point;

        // code
        $this->patch('/api/users/codeRecommend', [
            'code_recommend' => $this->other->code
        ])->assertStatus(200);

        $this->assertEquals($pointUser + User::$recommendPoint, $this->user->refresh()->point);
        $this->assertEquals($pointOther + User::$recommendPoint, $this->other->refresh()->point);
    }



    /** @test */
    public function 누구나_비밀번호를_초기화할_수_있다()
    {
        /*email
        contact (인증전화번호)
        password
        password_confirmation*/

        $password = 'testtest';

        $verifyNumber = VerifyNumber::create(['ids' => $this->user->contact, 'verified' => 1,'number'=>123]);

        $this->json('patch','/api/users/clearPassword', [
            'email' => $this->user->email,
            'contact' => $this->user->contact,
            'password' => $password,
            'password_confirmation' => $password,
        ]);

        $this->assertTrue(Hash::check($password, $this->user->refresh()->password));
    }

    /** @test */
    public function 사용자는_비밀번호를_수정할_수_있다()
    {
        /*password 기존 비밀번호
password_new
password_new_confirmation*/

        $prevPassword = 'testtest';
        $newPassword = 'test12341234';

        $this->user->update(['password' => Hash::make($prevPassword)]);

        $this->json('patch','/api/users/password', [
            'password' => $prevPassword,
            'password_new' => $newPassword,
            'password_new_confirmation' => $newPassword,
        ]);

        $this->assertTrue(Hash::check($newPassword, $this->user->refresh()->password));
    }

    /** @test */
    public function 누구나_아이디를_찾을_수_있다()
    {
        // contact (인증전화번호)
        $verifyNumber = VerifyNumber::create(['ids' => $this->user->contact, 'verified' => 1,'number'=>123]);

        $item = $this->json('patch','/api/users/findId', [
            'contact' => $this->user->contact,
        ])->decodeResponseJson()['data'];

        $this->assertEquals($this->user->email, $item['email']);
    }

    /** @test */
    public function 데이터에서_남은_포인트를_조회할_수_있다()
    {
        $points = Point::factory()->count(3)->create([
            'user_id' => $this->user->id,
            'point' => 200,
        ]);

        $this->assertEquals(600, $this->user->refresh()->point);
    }

    /** @test */
    public function 데이터에서_누적사용포인트를_조회할_수_있다()
    {
        $pointHistories = PointHistory::factory()->count(5)->create([
            'increase' => 0,
            'user_id' => $this->user->id,
            'point' => 200
        ]);

        $this->assertEquals(1000, $this->user->refresh()->point_use);
    }

    /** @test */
    public function 사용자는_자신의_정보를_수정할_수_있다()
    {
        /*password nullable
password_confirmation nullable
name
contact nullable (값이 있다면 인증된 번호여야함)
nikcname nullable
message
birth
count_family
agree_promotion*/

        $test = "test";

        $this->form = [
            'password' => null,
            'password_confirmation' => null,
            'name' => 'asd',
            'contact' => $this->user->contact,
            'nickname' => $test,
            'message' => 'asd',
            'birth' => '2000-03-03',
            'count_family' => 10,
            'agree_promotion' => 0,
        ];

        $this->json('patch', '/api/users', $this->form)->assertStatus(200);

        $this->assertEquals($test, $this->user->refresh()->nickname);
    }

    /** @test */
    public function 사용자에서_다음_등급_레벨업을_위해_남은_금액과_회차를_알_수_있다()
    {
        $grade = Grade::factory()->create();

        $nextGrade = Grade::factory()->create([
            'level' => $grade->level + 1,
            'min_price' => 100,
            'min_count_package' => 50
        ]);

        $this->user->update([
            'grade_id' => $grade->id,
            'total_order_price' => 30,
            'total_order_count_package' => 40,
        ]);

        $this->assertEquals(70, $this->user->refresh()->price_for_next_grade);
        $this->assertEquals(10, $this->user->refresh()->count_package_for_next_grade);
    }

    /** @test */
    public function 진행중인_출고가_있다면_탈퇴를_할_수_없다()
    {
        $preset = Preset::factory()->create([
            'user_id' => $this->user->id
        ]);

        $presetProducts = PresetProduct::create([
            'preset_id' => $preset->id,
            'state' => StatePresetProduct::READY
        ]);

        $this->json('delete', '/api/users')->assertStatus(403);
    }

    /** @test */
    public function 사용자가_탈퇴를_하면_대기중인_패키지출고는_삭제된다()
    {
        $packageSetting = PackageSetting::factory()->create([
            'user_id' => $this->user->id,
            'active' => 1,
        ]);

        $this->assertEquals(1, PresetProduct::count());

        $this->json('delete', '/api/users')->assertStatus(200);

        $this->assertEquals(0, PresetProduct::count());
    }
}
